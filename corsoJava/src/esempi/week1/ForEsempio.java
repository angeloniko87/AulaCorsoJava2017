package esempi.week1;
//esempio di ciclo for; utilizzato quanto conosciamo il valore del dato
public class ForEsempio {

	public static void main(String[] args) {
		int i;
		int a;
		
		for(i=0, a=9;i<10; i++, a--){
			System.out.print("vale:\t" +i);  //"print" non va nella linea successiva
			System.out.println("" +a);
			System.out.println("-----------------");
		
		
		}

	}	
}

